public class Entry
{
    private String name;
    private String number;
    
    public String getName()
    {
        return name;
    }
    
    public void setName(String theName)
    {
        name = theName;
    }
    
    public String getNumber()
    {
        return number;
    }
    
    public void setNumber(String theNumber)
    {
        number = theNumber;
    }
}
